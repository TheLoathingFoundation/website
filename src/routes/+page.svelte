<h2>Wait, What?</h2>

<p>
	The Loathing Foundation is a philanthropic community project from the <a
		href="https://discord.gg/kol"
		target="_blank"
		rel="noreferrer">Ascension Speed Society</a
	>. We provide a way for players to potentially access
	<a href="https://museum.loathers.net/player/3580284" target="_blank" rel="noreferrer"
		>past items of the month</a
	> without having to spend billions of real world dollars.
</p>

<h2>How does it work?</h2>

<p>
	Each month we invite some number of players to purchase one of the items that our loathing
	economists have deemed "available for distribution".
</p>
<p>
	Selected players will be able to trade <em
		>1 Mr. A + 3 Uncle Bucks for one "available" item of their choice</em
	>.
</p>
<p>
	The player gets the item, and The Loathing Foundation gets currency to buy future Items of the
	Month to add to the pool of future items!
</p>

<h2>Is this some kind of scam?</h2>

<p>
	No! This is genuinely meant to be a source of discounted Items of the Month. <em
		>Nobody is making a profit from this</em
	>, and all proceeds from trades are used to buy more IOTMs for future players. Trades are
	performed via the KoL trade interface.
</p>
<p>You can learn more about how it works in <a href="faq">the FAQ</a>.</p>

<h2>What's the catch?</h2>
<p>If you obtain an item through The Loathing Foundation <em>you must use it</em>.</p>
<p><em><b>Do not sell it</b></em>.</p>
<p>
	We can't force you to do the right thing, but if we learn that you're taking advantage of this
	crazy idea to flip items and make a personal profit then you will have made us all very sad (and
	you'll be ineligible to trade for items in future).
</p>


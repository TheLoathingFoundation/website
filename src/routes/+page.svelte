<h2>Wait, What?</h2>

<p>The Loathing Foundation is a philanthropic community project from the <a href="https://discord.gg/kol" target="_blank" rel="noreferrer">Ascension Speed Society</a>. We provide a way for players to potentially access <a href="https://museum.loathers.net/player/3580284" target="_blank" rel="noreferrer">past items of the month</a> without having to spend billions of real world dollars.</p>

<h2>How does it work?</h2>

<p>Each month we invite some number of members to purchase one of the items that our loathing economists have deemed "available for distribution".</p>
<p>Selected players will be able to trade <em>1 Mr. A + 3 Uncle Bucks for one "available" item of their choice</em>.</p>
<p>The player gets the item, and The Loathing Foundation gets currency to buy future Items of the Month to add to the pool of future items!</p>

<h2>Is this some kind of scam?</h2>

<p>No!</p>

<h2>What's the catch?</h2>

<p>If you obtain an item through The Loathing Foundation <em>you must use it</em>.</p>
<p><em><b>Do not sell it</b> -- and don't sell copies of it in the future</em>.</p>
<p>If we see a copy of an item you got from us in your mall store then you will have made us all very sad AND your membership to the foundation will be permanently revoked, leaving you forever ineligable to make future trades.</p>

